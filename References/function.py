# function with two arguments
def sub_numbers(num1, num2):
    difference = num1 - num2
    print ("difference: ",difference)

# function call with two values
sub_numbers(5, 4)

# function to calculate powers
def exponent(base, n):
    exp_number = base ** n 
    print("Exponent: ",exp_number)

# function call with two values
exponent(9, 0)
exponent(5, 2)

